package receiver;

public class Selection {
	private int start;
	private int lenght;
	
	public Selection() {
		this.start = 0;
		this.lenght = 0;
	}

	public int getStart() {
		return start; 
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getLenght() {
		return lenght;
	}

	public void setLenght(int lenght) {
		this.lenght = lenght;
	}

	
}
